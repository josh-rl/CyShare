/**
 * All available roles for the current implementation of our app.
 * @author Hugo Alvarez Valdivia
 */
public enum Role {
    /**
     * ADMIN: User entity with administrator privileges.
     * PASSENGER: User entity with passenger-only privileges.
     * DRIVER: User entity with driver-only privileges.
     */
    ADMIN, PASSENGER, DRIVER;
}
